#include "../../include/libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == -2147483648)
	{
		ft_putchar_fd('-', fd);
		ft_putchar_fd('2', fd);
		n = 147483648;
	}
	else if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n = -n;
	}
	if (10 <= n)
	{
		ft_putnbr_fd(n / 10, fd);
	}
	ft_putchar_fd((char)(n % 10 + '0'), fd);
}
