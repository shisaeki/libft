#include "../../include/libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	unsigned char	*tmp;

	tmp = (unsigned char *)b;
	while (0 < len)
	{
		*tmp = c;
		tmp++;
		len--;
	}
	return (b);
}